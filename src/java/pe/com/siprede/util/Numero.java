package pe.com.siprede.util;

/**
 *
 * @author Cristian1312
 */
public class Numero {

    public static double redondear(double numero, int digitos) {
        int cifras = (int) Math.pow(10, digitos);
        return Math.rint(numero * cifras) / cifras;
    }
}
